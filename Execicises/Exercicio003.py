# Crie um programa que leia dois números e mostre a soma entre eles.

num1 = float(input('Entre com o primeiro numero: '))
num2 = float(input('Entre com o segundo numero: '))

print('A soma do primeiro numero digitado foi: {}\n'.format(num1), 'O segundo numero digitado foi: {}\n'.format(num2), 'A soma desses numeros é: {}'.format(num1+num2))
